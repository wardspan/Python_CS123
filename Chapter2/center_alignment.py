# This program demonstrates alignment of text in strings
name1 = "John"
name2 = "Doe"
name3 = "Smith"
name4 = "Johnson"
name5 = "Williams"
name6 = "Brown"

#display the names with proper alignment
print('Examples of Center Alignment')
print()
print(f'***{name1:^20}***')
print(f'***{name2:^20}***')
print(f'***{name3:^20}***')
print(f'***{name4:^20}***')
print(f'***{name5:^20}***')
print(f'***{name6:^20}***')
print()
print('Examples of Left Alignment')
print()
print(f'***{name1:<20}***')
print(f'***{name2:<20}***')
print(f'***{name3:<20}***')
print(f'***{name4:<20}***')
print(f'***{name5:<20}***')
print(f'***{name6:<20}***')
print()
print('Examples of Right Alignment')
print()
print(f'***{name1:>20}***')
print(f'***{name2:>20}***')
print(f'***{name3:>20}***')
print(f'***{name4:>20}***')
print(f'***{name5:>20}***')
print(f'***{name6:>20}***')
